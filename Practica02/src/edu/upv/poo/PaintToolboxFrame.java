    public void addToolsListener(ToolsListener l) {
        if (l == null) return;
        toolsListeners.add(l);
    }
    
    public void removeToolsListener(ToolsListener l) {
        if (l == null) return;
        toolsListeners.remove(l);
    }
    
    private void notifyColorChanged(Color color, String colorName) {
        for (ToolsListener l : toolsListeners) {
            ColorChangedEvent evt = 
                    new ColorChangedEvent(this, color, colorName);
            l.colorChanged(evt);
        }
    }
    
    private void notifySizeChanged(int actualSize) {
        for (ToolsListener l : toolsListeners) {
            SizeChangedEvent e = new SizeChangedEvent(this, actualSize);
            l.sizeChanged(e);
        }
    }
    
    private void notifyToolChanged(String tool) {
        for (ToolsListener l : toolsListeners) {
            l.toolChanged(new ToolChangedEvent(this, tool));
        }
    }

